import java.lang.Math;
public class forever {
    public static void main (String[] args){
        System.out.println("a  a^2  a^3");
        System.out.println(1 + "  " + 1 * 1 + "  " + 1 * 1 * 1);
        System.out.println(2 + "  " + 2 * 2 + "  " + 2 * 2 * 2);
        System.out.println(3 + "  " + 3 * 3 + "  " + 3 * 3 * 3);
        System.out.println(4 + "  " + 4 * 4 + "  " + 4 * 4 * 4);
        System.out.println(1 + "  " + Math.pow(1, 2) + "  " + Math.pow(1, 3));
        System.out.println(2 + "  " + Math.pow(2, 2) + "  " + Math.pow(2, 3));
        System.out.println(3 + "  " + Math.pow(3, 2) + "  " + Math.pow(3, 3));
        System.out.println(4 + "  " + Math.pow(4, 2) + "  " + Math.pow(4, 3));
    }
}
